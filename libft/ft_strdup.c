#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*copy;
	int		i;

	i = 0;
	len = ft_strlen(s) + 1;
	copy = (char *)malloc(sizeof(char) * len);
	if (!copy)
		return (NULL);
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
